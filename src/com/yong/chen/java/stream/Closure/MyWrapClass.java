package com.yong.chen.java.stream.Closure;

public class MyWrapClass {
	
	private Integer myValue;

	public MyWrapClass(Integer myValue) {
		super();
		this.myValue = myValue;
	}

	public Integer getMyValue() {
		return myValue;
	}

	public void setMyValue(Integer myValue) {
		this.myValue = myValue;
	}
	
	

}
